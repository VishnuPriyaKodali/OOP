import java.io.*;
class Widening
 { 
public static void main(String[] args) 
{
 int i = 10;
 // Widening TypeCasting (Automatic Casting)
 // from int to long 
long l = i;
 // Widening TypeCasting (Automatic Casting)
 // from int to double
 double d = i;
 System.out.println("Integer: " + i); 
System.out.println("Long: " + l); 
System.out.println("Double: " + d); 
}
}
